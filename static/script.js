// ── Cookie Upload ──────────────────────────────────────
cookiesFile.addEventListener("change", async (e) => {
  const file = e.target.files[0];
  if (!file) return;
  const formData = new FormData();
  formData.append("file", file);
  try {
    const res = await fetch("/api/upload-cookies", { method: "POST", body: formData });
    const data = await res.json();
    if (res.ok) {
      showToast("Cookies uploaded! Retrying...", "success");
      cookiesBanner.hidden = true;
      if (urlInput.value.trim()) {
        fetchInfo();
      }
    } else {
      showToast(data.detail || "Invalid cookies file", "error");
    }
  } catch (err) {
    showToast("Upload failed: " + err.message, "error");
  }
  cookiesFile.value = "";
});

formatList.addEventListener("click", (e) => {
  const item = e.target.closest(".format-item");
  if (!item || item.classList.contains("downloading")) return;

  // Brief visual feedback
  item.style.transform = "scale(0.98)";
  setTimeout(() => { item.style.transform = ""; }, 150);

  downloadFormat(item.dataset.id, item.dataset.ext, item.dataset.type, item.dataset.label, item.dataset.source, item.dataset.videoId);
});
