// ── Helper: format bytes/s to human readable ──────────
function formatSpeed(bytesPerSec) {
  if (bytesPerSec >= 1024 * 1024) return `${(bytesPerSec / (1024 * 1024)).toFixed(1)} MB/s`;
  if (bytesPerSec >= 1024) return `${(bytesPerSec / 1024).toFixed(0)} KB/s`;
  return `${bytesPerSec} B/s`;
}

function formatEta(seconds) {
  if (!seconds || seconds === Infinity) return "--:--";
  const m = Math.floor(seconds / 60);
  const s = Math.floor(seconds % 60);
  if (m > 60) return `${Math.floor(m / 60)}h ${m % 60}m`;
  return `${m}:${s.toString().padStart(2, "0")}`;
}

function formatSize(bytes) {
  if (bytes >= 1024 * 1024 * 1024) return `${(bytes / (1024 * 1024 * 1024)).toFixed(2)} GB`;
  if (bytes >= 1024 * 1024) return `${(bytes / (1024 * 1024)).toFixed(1)} MB`;
  if (bytes >= 1024) return `${(bytes / 1024).toFixed(0)} KB`;
  return `${bytes} B`;
}

// ── Download with Progress ──────────────────────────────
let activeDownload = null;

function downloadFormat(formatId, ext, type, label, source, videoId) {
  let downloadUrl;
  if (source === "invidious" && videoId) {
    downloadUrl = `/api/download-invidious?video_id=${encodeURIComponent(videoId)}&itag=${encodeURIComponent(formatId)}&ext=${encodeURIComponent(ext)}`;
  } else {
    const url = urlInput.value.trim();
    downloadUrl = `/api/download?url=${encodeURIComponent(url)}&format_id=${encodeURIComponent(formatId)}&ext=${encodeURIComponent(ext)}&download_type=${encodeURIComponent(type)}`;
  }

  showDownloadOverlay(label, ext, downloadUrl);
}

function showDownloadOverlay(label, ext, downloadUrl) {
  // Create overlay
  const overlay = document.createElement("div");
  overlay.className = "dl-overlay";
  overlay.innerHTML = `
    <div class="dl-overlay-inner">
      <div class="dl-overlay-left">
        <div class="dl-spinner"><div class="dl-spinner-ring"></div></div>
      </div>
      <div class="dl-overlay-right">
        <div class="dl-filename">${escapeHtml(label)}<span class="dl-ext">.${ext}</span></div>
        <div class="dl-status">Preparing download...</div>
        <div class="dl-progress-track"><div class="dl-progress-fill"></div></div>
        <div class="dl-stats">
          <span class="dl-stat-speed"></span>
          <span class="dl-stat-size"></span>
          <span class="dl-stat-eta"></span>
        </div>
      </div>
      <button class="dl-close-btn" title="Cancel">
        <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><line x1="18" y1="6" x2="6" y2="18"/><line x1="6" y1="6" x2="18" y2="18"/></svg>
      </button>
    </div>
  `;
  document.body.appendChild(overlay);

  const fill = overlay.querySelector(".dl-progress-fill");
  const status = overlay.querySelector(".dl-status");
  const speedEl = overlay.querySelector(".dl-stat-speed");
  const sizeEl = overlay.querySelector(".dl-stat-size");
  const etaEl = overlay.querySelector(".dl-stat-eta");
  const closeBtn = overlay.querySelector(".dl-close-btn");
  const spinner = overlay.querySelector(".dl-spinner");

  let cancelled = false;
  // Cancel any previous download
  if (activeDownload) activeDownload.abort();
  let controller = new AbortController();
  activeDownload = controller;

  closeBtn.addEventListener("click", () => {
    cancelled = true;
    controller.abort();
    overlay.remove();
    activeDownload = null;
  });

  // Simulate progress while server prepares
  const prepSteps = [
    { t: 500, text: "Connecting to server..." },
    { t: 2000, text: "Fetching from YouTube..." },
    { t: 5000, text: "Downloading video..." },
    { t: 10000, text: "Processing & merging..." },
    { t: 20000, text: "Almost ready..." },
  ];
  const prepTimers = prepSteps.map(s =>
    setTimeout(() => { if (!cancelled) status.textContent = s.text; }, s.t)
  );

  fetch(downloadUrl, { signal: controller.signal })
    .then(response => {
      prepTimers.forEach(clearTimeout);
      if (cancelled || !response.ok) {
        return response.json().catch(() => null).then(d => { throw new Error((d && d.detail) || `Server error (${response.status})`); });
      }

      const contentLength = parseInt(response.headers.get("Content-Length") || "0", 10);
      const reader = response.body.getReader();
      const chunks = [];
      let received = 0;
      let startTime = Date.now();
      let lastTime = startTime;
      let lastBytes = 0;

      // Switch to real progress mode
      spinner.classList.add("dl-spinner-active");
      status.textContent = "Downloading...";
      if (contentLength > 0) sizeEl.textContent = `0 / ${formatSize(contentLength)}`;

      function pump() {
        return reader.read().then(({ done, value }) => {
          if (done) return finish();
          if (cancelled) return;

          chunks.push(value);
          received += value.length;
          const now = Date.now();
          const elapsed = (now - lastTime) / 1000;

          // Update every 200ms to avoid flicker
          if (elapsed >= 0.2) {
            const speed = (received - lastBytes) / elapsed;
            const pct = contentLength > 0 ? Math.round((received / contentLength) * 100) : 0;
            const elapsedTotal = (now - startTime) / 1000;
            const eta = contentLength > 0 && speed > 0 ? ((contentLength - received) / speed) : null;

            fill.style.width = `${pct}%`;
            speedEl.textContent = formatSpeed(speed);
            sizeEl.textContent = contentLength > 0
              ? `${formatSize(received)} / ${formatSize(contentLength)}`
              : formatSize(received);
            if (eta !== null) etaEl.textContent = `${formatEta(eta)} left`;
            status.textContent = `${pct}% downloaded`;

            lastTime = now;
            lastBytes = received;
          }

          return pump();
        });
      }

      function finish() {
        if (cancelled) return;
        fill.style.width = "100%";
        status.textContent = "Saving file...";
        speedEl.textContent = "";
        etaEl.textContent = "";

        const blob = new Blob(chunks);
        const blobUrl = URL.createObjectURL(blob);
        const disposition = response.headers.get("Content-Disposition") || "";
        let filename = `${label}.${ext}`;
        const match = disposition.match(/filename\*=utf-8''([^;]+)/i);
        if (match) filename = decodeURIComponent(match[1].replace(/^utf-8''/, ''));
        else {
          const m2 = disposition.match(/filename[="']?([^;"']+)/i);
          if (m2) filename = m2[1].replace(/['"]/g, "");
        }

        const a = document.createElement("a");
        a.href = blobUrl;
        a.download = filename;
        document.body.appendChild(a);
        a.click();
        document.body.removeChild(a);
        setTimeout(() => URL.revokeObjectURL(blobUrl), 2000);

        // Success state
        overlay.classList.add("dl-overlay-success");
        status.textContent = "Download complete";
        sizeEl.textContent = formatSize(received);
        spinner.innerHTML = `<svg viewBox="0 0 24 24" fill="none" stroke="#22C55E" stroke-width="3" stroke-linecap="round" stroke-linejoin="round" width="28" height="28"><polyline points="20 6 9 17 4 12"/></svg>`;

        setTimeout(() => {
          overlay.style.opacity = "0";
          overlay.style.transform = "translateX(-50%) translateY(10px)";
          setTimeout(() => overlay.remove(), 300);
        }, 2500);

        showToast(`"${filename}" saved`, "success");
        activeDownload = null;
      }

      return pump();
    })
    .catch(err => {
      prepTimers.forEach(clearTimeout);
      if (cancelled || err.name === "AbortError") return;
      overlay.remove();
      activeDownload = null;
      showToast(err.message || "Download failed", "error");
    });
}

